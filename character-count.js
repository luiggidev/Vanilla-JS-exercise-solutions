// Adiciona conteudo dos seletores em variaveis
var text = document.querySelector('#text');
var charCount = document.querySelector('#character-count'); 
//test
var digitCount = document.querySelector('#digit-count');

// Adiciona uma escuta no evento input pra variavel 'text'
text.addEventListener('input', function() {

	charCount.textContent = text.value.length;
	// test
	digitCount.textContent = charCount.textContent.toString().length;

}, false);
